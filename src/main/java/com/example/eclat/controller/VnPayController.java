    @GetMapping("/create")
    public String createPayment(@RequestParam int amount,
                                @RequestParam String orderInfo,
                                @RequestParam Long orderId,
                                HttpServletRequest request) throws Exception {
        String ipAddress = request.getRemoteAddr();
        if ("0:0:0:0:0:0:0:1".equals(ipAddress)) {
            ipAddress = "127.0.0.1";
        }

        // L·∫•y th√¥ng tin ƒë∆°n h√†ng
        Optional<Order> orderOpt = orderService.getOrderById(orderId);
        if (orderOpt.isEmpty()) {
            throw new IllegalArgumentException("Order kh√¥ng t·ªìn t·∫°i!");
        }
        Order order = orderOpt.get();

        // Ki·ªÉm tra n·∫øu ƒë√£ c√≥ giao d·ªãch cho ƒë∆°n h√†ng n√†y
        Optional<Transaction> existingTransaction = transactionRepository.findByOrder(order);
        if (existingTransaction.isPresent()) {
            throw new IllegalStateException("ƒê∆°n h√†ng n√†y ƒë√£ c√≥ giao d·ªãch!");
        }

        // T·∫°o m√£ giao d·ªãch duy nh·∫•t (txnRef)
        String txnRef = vnPayService.generateTxnRef(); // Vi·∫øt h√†m n√†y trong VnPayService

        // T·∫°o transaction tr·∫°ng th√°i PENDING
        Transaction transaction = Transaction.builder()
                .order(order)
                .amount(BigDecimal.valueOf(amount))
                .transactionStatus("PENDING")
                .paymentMethod("VNPAY")
                .vnpTxnRef(txnRef)
                .createAt(LocalDateTime.now())
                .build();

        transactionRepository.save(transaction);

        // T·∫°o URL thanh to√°n v√† tr·∫£ v·ªÅ
        return vnPayService.createPaymentUrl(amount, orderInfo, ipAddress, txnRef);
    }


    @GetMapping("/vnpay-return")
    public ResponseEntity<?> handleReturnUrl(HttpServletRequest request) {
        try {
            // üîπ T·∫°o b·∫£n sao Mutable c·ªßa request.getParameterMap()
            Map<String, String[]> params = new HashMap<>(request.getParameterMap());

            String vnpTxnRef = request.getParameter("vnp_TxnRef");
            String vnpResponseCode = request.getParameter("vnp_ResponseCode");

            // üîπ X√°c th·ª±c ch·ªØ k√Ω VNPAY
            boolean isValid = vnPayService.validateSignature(params);
            if (!isValid) {
                return ResponseEntity.badRequest().body("‚ùå X√°c th·ª±c ch·ªØ k√Ω kh√¥ng h·ª£p l·ªá!");
            }

            // üîπ L·∫•y giao d·ªãch t·ª´ DB
            Optional<Transaction> transactionOpt = transactionRepository.findByVnpTxnRef(vnpTxnRef);
            if (transactionOpt.isEmpty()) {
                return ResponseEntity.status(HttpStatus.NOT_FOUND).body("‚ùå Kh√¥ng t√¨m th·∫•y giao d·ªãch!");
            }

            Transaction transaction = transactionOpt.get();
            Order order = transaction.getOrder();

            // üîπ X√°c ƒë·ªãnh tr·∫°ng th√°i giao d·ªãch
            String status = "00".equals(vnpResponseCode) ? "SUCCESS" : "FAILED";
            transaction.setTransactionStatus(status);
            transaction.setVnpResponseCode(vnpResponseCode);

            // üîπ C·∫≠p nh·∫≠t tr·∫°ng th√°i ƒë∆°n h√†ng n·∫øu thanh to√°n th√†nh c√¥ng
            if ("SUCCESS".equals(status)) {
                order.setStatus("PAID");
                orderService.save(order);
            }

            // üîπ L∆∞u transaction v√†o DB
            transactionRepository.save(transaction);

            return ResponseEntity.ok("‚úÖ Thanh to√°n " + status);
        } catch (Exception e) {
            e.printStackTrace();
            return ResponseEntity.status(500).body("‚ùå L·ªói h·ªá th·ªëng: " + e.getMessage());
        }
    }



